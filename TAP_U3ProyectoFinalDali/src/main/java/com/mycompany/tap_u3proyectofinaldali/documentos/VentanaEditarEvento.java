    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        Date fecha = new Date(
                Integer.parseInt(this.textYear.getText()), 
                Integer.parseInt(this.textMes.getText()), 
                Integer.parseInt(this.textDia.getText()));
        Time hora = new Time(Integer.parseInt(this.textHora.getText()), Integer.parseInt(this.textMinutos.getText()), 0);
        
        String nombre = this.textNombre.getText();
        
        this.evento.setFecha(fecha);
        this.evento.setHora(hora);
        this.evento.setNombre(nombre);
        
        try {
            if (this.ws.updateEvent(evento, new ArrayList<Participante>())) {
                this.ventanaPrincipalDocumentos.acutalizarVentana();
                JOptionPane.showMessageDialog(null, "Se edito el evento correctamente");
                this.dispose();
            } else {
                 JOptionPane.showMessageDialog(null, "Error al editar el evento");
            }
        } catch (IOException ex) {
            Logger.getLogger(VentanaEditarEvento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

